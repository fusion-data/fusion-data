//! 集成测试模块
//!
//! 本模块包含 hetuflow-server 与 hetuflow-agent 的完整交互集成测试
//!
//! ## 测试场景
//!
//! 1. **Agent 注册与心跳测试** - 验证 Agent 成功注册到 Server 并维持心跳连接
//! 2. **任务轮询与获取测试** - 测试 Agent 通过 labels 匹配获取任务的完整流程
//! 3. **任务执行状态更新测试** - 验证 Agent 执行任务过程中的状态同步机制
//! 4. **任务完成与结果上报测试** - 测试任务执行完成后结果回传给 Server
//! 5. **错误处理与重试机制测试** - 验证网络异常、任务失败等场景的处理
//! 6. **并发场景测试** - 测试多个 Agent 同时获取任务的竞争条件处理
//! 7. **性能与压力测试** - 验证大量任务和多 Agent 场景下的系统性能
